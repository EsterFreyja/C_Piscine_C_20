int		ft_str_is_alpha(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 90 && str[i] < 97) || str[i] > 122)
			j++;
		if (str[i] == '\0' || str[i] < 65)
			j++;
		i++;
	}
	if (i == 0 || j == 0)
		return (1);
	else
		return (0);
}
